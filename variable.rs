fn main() {
    let _x = 90; //immutable
    println!("x");
    let mut a = 5;  //mutable
    println!("The value of a is {}", a );
    a = 10;
    println!("The new value of a is {}", a);
    let mut b = "i am guru";
    println!("{}", b );
    b = "guru now GURU";
    print!("{}",b) //println! just adds a new line
}
